using System;

namespace _2dThing
{
	public class Input
	{
		public bool Left;
		public bool Right;
		public bool Up;
		public bool Down;
		public bool LeftMouseButton;
		public bool RightMouseButton;
		
		public Input ()
		{
			Left = false;
			Right = false;
			Up = false;
			Down = false;
			LeftMouseButton = false;
			RightMouseButton = false;
		}
	}
}

